

function signshow()
{
    
    var psw = document.getElementById("password");
   
    var psw2 = document.getElementById("cpassword");
    var eye = document.getElementById("eye");    
    if(psw.type == "password")
    {
        psw.type="text"
        psw2.type="text"
        eye.classList.toggle('active');
    }
    else
    {
        psw.type="password"
        psw2.type="password"
        eye.classList.toggle('active');  
         
    }        
}
